
public class OperationsTest 
{

	public static void main(String[] args) 
	{
		int number; //Declaration statement
		number = 20; //Assignment statement -> initialization
		number+=5; //Spaces do not matter; could also do number = number + 5 for this line since it is read r -> l
		System.out.println(number);
		number/=2;
		System.out.println(number);
		number-=6;
		System.out.println(number);
		number*=4;
		System.out.println(number);
		number %= 2;
		System.out.println(number);

	}

}
